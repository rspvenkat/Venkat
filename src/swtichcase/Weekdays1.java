package swtichcase;

public class Weekdays1 {
	public static void main(String[]args) {
		String day="monday";
		switch(day) {
		case  "sunday":System.out.println("1st day");break;
		case "monday":System.out.println("2st day");break;
		case "tesday":System.out.println("3st day");break;
		case "wensday":System.out.println("4st day");break;
		case "thurday":System.out.println("5st day");break;
		case "friday":System.out.println("6st day");break;
		default : System.out.println("invalid");





		}
	}

}
